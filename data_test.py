"""
data_test.py

Test-time data loading for Catalytic Site Prediction (Set A 3D-CNN model
applied to setB_test).

setB_test uses a different file naming convention than setA_train (which is
what data.py is built for):

  setA_train (data.py):
    - PDB filenames look like '1a4i_A.pdb'
    - chain id is parsed from pdb_file[5]
    - ESM filenames are 'sp|<pdb4>_<chain>|esm.pt'
    - neighbour residues are looked up by (PDB resid - 1)

  setB_test (this file):
    - PDB filenames look like 'A0A084JZF2.pdb' (UniProt accession)
    - chain id comes from the CSV (always 'A' in setB)
    - ESM filenames are 'sp|<uniprot>|esm.pt' (no chain suffix)
    - neighbour residues are looked up by sequential index, since the ESM
      was generated by PPBuilder over the PDB ATOM sequence

Exports:
    - get_pos_feature_setB
    - ReadfromCSV_SetB
"""

import logging
from pathlib import Path

import numpy as np
import pandas as pd
import torch
from torch.utils.data import Dataset

from Bio.PDB import PDBParser

from data import AA_3to1_dict, get_residue_center


# ============================================================================
# Feature extraction (setB-flavored)
# ============================================================================
def get_pos_feature_setB(pdb_file, box_size, voxel_size, pdb_dir, chain,
                         res_num, esm_dir, esm_layer):
    """Build a 3D voxel feature tensor centered on a target residue (setB).

    Same logic as data.get_pos_feature, but
      * looks up the ESM file as 'sp|<pdb_name>|esm.pt' (no chain suffix)
      * indexes neighbour ESM rows by sequential order, not (resid - 1)

    Returns float32 array of shape (numbox, numbox, numbox, embedding_dim).
    """
    numbox = int(box_size / voxel_size)
    pdb_path = str(Path(pdb_dir) / pdb_file)
    pdb_name = pdb_file.split(".")[0]

    parser = PDBParser(QUIET=True)
    structure = parser.get_structure(pdb_name, pdb_path)
    chain_obj = structure[0][chain]

    # ---- Build PDB resid -> sequential order index for this chain ----
    residue_to_order_index = {}
    order = 0
    for residue in chain_obj:
        het, resid, icode = residue.get_id()
        if het == ' ':
            residue_to_order_index[resid] = order
            order += 1

    # ---- Load ESM embeddings ----
    esm_file = "{}sp|{}|esm.pt".format(esm_dir, pdb_name)
    try:
        esm_record = torch.load(esm_file, map_location="cpu")
        if "representations" in esm_record:
            esm_embeddings = esm_record["representations"][esm_layer]
        else:
            first = next(iter(esm_record.values()))
            esm_embeddings = first["representations"][esm_layer]
    except FileNotFoundError:
        logging.warning("ESM file not found: %s", esm_file)
        return np.zeros((numbox, numbox, numbox, 2560), dtype=np.float32)
    except KeyError:
        logging.error("ESM layer %s not found in %s", esm_layer, esm_file)
        return np.zeros((numbox, numbox, numbox, 2560), dtype=np.float32)

    embedding_dim = esm_embeddings.shape[1]
    esm_len = esm_embeddings.shape[0]

    # ---- Sanity-check the target residue ----
    try:
        residue = structure[0][chain][res_num]
    except KeyError:
        logging.warning("%s: chain %s residue %s not found", pdb_file, chain, res_num)
        return np.zeros((numbox, numbox, numbox, embedding_dim), dtype=np.float32)

    amino_acid_3 = residue.get_resname()
    if amino_acid_3 not in AA_3to1_dict:
        logging.warning("%s: %s%s is not a standard amino acid",
                        pdb_file, res_num, amino_acid_3)

    residue_center = get_residue_center(residue)
    if residue_center is None:
        logging.warning("%s: %s%s has no reliable center",
                        pdb_file, res_num, amino_acid_3)
        return np.zeros((numbox, numbox, numbox, embedding_dim), dtype=np.float32)

    # ---- Fill voxels with neighbor ESM embeddings ----
    struct_feature = np.zeros((numbox, numbox, numbox, embedding_dim),
                              dtype=np.float32)
    for nb_residue in structure.get_residues():
        nb_center = get_residue_center(nb_residue)
        if nb_center is None:
            continue

        delta_x = nb_center[0] - residue_center[0]
        delta_y = nb_center[1] - residue_center[1]
        delta_z = nb_center[2] - residue_center[2]

        if abs(delta_x) < 10 and abs(delta_y) < 10 and abs(delta_z) < 10:
            _, nb_resid, _ = nb_residue.get_id()
            if nb_resid in residue_to_order_index:
                order_idx = residue_to_order_index[nb_resid]
                if 0 <= order_idx < esm_len:
                    nb_embedding = esm_embeddings[order_idx]

                    voxel_x = int(delta_x / voxel_size) + int(numbox / 2)
                    voxel_y = int(delta_y / voxel_size) + int(numbox / 2)
                    voxel_z = int(delta_z / voxel_size) + int(numbox / 2)
                    if (0 <= voxel_x < numbox
                            and 0 <= voxel_y < numbox
                            and 0 <= voxel_z < numbox):
                        struct_feature[voxel_x, voxel_y, voxel_z, :] = nb_embedding

    return struct_feature


# ============================================================================
# Dataset (setB-flavored)
# ============================================================================
class ReadfromCSV_SetB(Dataset):
    """Test-time Dataset for setB-style CSV / PDB / ESM layouts.

    Returns the same (feat, label, sample_id) triple as data.ReadfromCSV so
    it can be plugged directly into evaluate.evaluate_tensor_data.
    """

    def __init__(self, csv_path, pdb_dir, esm_dir, esm_layer=36,
                 box_size=20, voxel_size=1, debug_max_samples=None,
                 verify_lengths=True):
        self.csv_path = csv_path
        self.pdb_dir = pdb_dir
        self.esm_dir = esm_dir
        self.esm_layer = esm_layer
        self.box_size = box_size
        self.voxel_size = voxel_size

        self.df = pd.read_csv(csv_path)
        if debug_max_samples is not None:
            self.df = self.df.head(debug_max_samples)
            print("Debug mode: limited to {} samples".format(debug_max_samples))

        if verify_lengths:
            self._filter_missing_esm()

        self.df = self.df.reset_index(drop=True)
        self.pos_num = (self.df['label'] == 1.0).sum()
        self.neg_num = (self.df['label'] == 0.0).sum()
        print("Loaded test CSV: {} samples".format(len(self.df)))
        print("  Positive: {}".format(self.pos_num))
        print("  Negative: {}".format(self.neg_num))

    def _filter_missing_esm(self):
        """Drop rows whose ESM file is missing on disk.

        setB_test ESM was generated from PPBuilder(PDB sequence), so length
        mismatches are not expected here. We only filter out PDB files for
        which the ESM file does not exist.
        """
        unique_pdbs = self.df['pdb_file'].unique()
        valid_pdbs = set()
        missing = 0
        for pdb_file in unique_pdbs:
            pdb_name = pdb_file.split(".")[0]
            esm_path = Path(self.esm_dir) / "sp|{}|esm.pt".format(pdb_name)
            if esm_path.exists():
                valid_pdbs.add(pdb_file)
            else:
                missing += 1
                logging.warning("Missing ESM for %s -> %s", pdb_file, esm_path)

        before = len(self.df)
        self.df = self.df[self.df['pdb_file'].isin(valid_pdbs)]
        dropped = before - len(self.df)
        print("Filter: dropped {} rows ({} PDBs without ESM); "
              "kept {} rows from {} PDBs".format(
                  dropped, missing, len(self.df), len(valid_pdbs)))

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        pdb_file = row['pdb_file']
        chain = str(row['chain'])
        res_num = int(row['res_num'])
        label = float(row['label'])

        feat = get_pos_feature_setB(
            pdb_file=pdb_file,
            box_size=self.box_size,
            voxel_size=self.voxel_size,
            pdb_dir=self.pdb_dir,
            chain=chain,
            res_num=res_num,
            esm_dir=self.esm_dir,
            esm_layer=self.esm_layer,
        )
        sample_id = "{}|{}|{}".format(pdb_file, chain, res_num)
        return feat, label, sample_id
